    private void jButtonSalvarVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarVeiculoActionPerformed
        if (jTextFieldModeloVeiculo.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Modelo não pode ser vazio!");
            jTextFieldModeloVeiculo.requestFocus();
            return;
        }

        if (jTextFieldPlacaVeiculo.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Placa não pode ser vazio!");
            return;
        }

        if (jComboBoxFabricanteVeiculo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Fabricante não pode ser vazio!");
        }

        Veiculo v = new Veiculo();
        v.setModeloVeiculo(jTextFieldModeloVeiculo.getText());
        v.setPlacaVeiculo(jTextFieldPlacaVeiculo.getText());
        v.setFabricanteVeiculo(String.valueOf(jComboBoxFabricanteVeiculo.getSelectedItem()));
        v.setEstaDisponivel(jRadioButtonVeiculoDisponivelSim.isSelected());
        v.setFKTipoVeiculo((TipoVeiculo) jComboBoxTipoVeiculo.getSelectedItem());

        if (jLabelNumId.getText().equals("")) {
            int codInserir = new VeiculoDAO().inserir(v);
            if (codInserir == Utilitarios.NAO_FOI_POSSIVEL_INSERIR) {
                JOptionPane.showMessageDialog(null, "Não foi Possível Inserir");
            } else {
                v.setIdVeiculo(codInserir);
                jLabelNumId.setText(String.valueOf(codInserir));
                JOptionPane.showMessageDialog(null, "Veículo Inserido com Sucesso");
            }
        } else {
            int id = Integer.parseInt(jLabelNumId.getText());
            v.setIdVeiculo(id);
            int codAlteracao = new VeiculoDAO().alterar(v);
            if (codAlteracao == Utilitarios.NAO_FOI_POSSIVEL_ALTERAR) {
                JOptionPane.showMessageDialog(null, "Não foi Possível Alterar");
            } else {
                JOptionPane.showMessageDialog(null, "Veículo Alterado com Sucesso");
            }
        }
        dispose();
    }//GEN-LAST:event_jButtonSalvarVeiculoActionPerformed

