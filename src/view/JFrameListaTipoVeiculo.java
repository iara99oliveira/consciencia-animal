    private void jButtonExcluirTipoVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirTipoVeiculoActionPerformed
        if (jTableTipoVeiculo.getSelectedRow() > -1) {
            int codTipoVeiculo = Integer.parseInt(jTableTipoVeiculo.getValueAt(jTableTipoVeiculo.getSelectedRow(), 0).toString());
            int excluido = new TipoVeiculoDAO().excluir(codTipoVeiculo);
            if (excluido != Utilitarios.NAO_FOI_POSSIVEL_EXCLUIR) {
                popularListaTipoVeiculo();
                JOptionPane.showMessageDialog(null, "Tipo de Veiculo excluido com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "Não foi possível excluir a Tipo de Veiculo");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione algo para excluir");
        }
      
        
        
    }//GEN-LAST:event_jButtonExcluirTipoVeiculoActionPerformed

