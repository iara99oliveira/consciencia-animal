    private void jButtonAdicionarSalvamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarSalvamentoActionPerformed
        if (jTableOcorrencia.getSelectedRow() > -1) {

            int codigoOcorrencia = Integer.parseInt(jTableOcorrencia.getValueAt(jTableOcorrencia.getSelectedRow(), 0).toString());

            Salvamento s = new Salvamento();
            Ocorrencia o = new OcorrenciaDAO().buscarOcorrenciaPorId(codigoOcorrencia);
            s.setFKOcorrencia(o);
            new JFrameCadastroSalvamento(s).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um salvamento ja realizado");
        }
    }//GEN-LAST:event_jButtonAdicionarSalvamentoActionPerformed

