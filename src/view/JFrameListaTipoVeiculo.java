    private void jButtonEditarTipoVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarTipoVeiculoActionPerformed
        if (jTableTipoVeiculo.getSelectedRow() > -1) {

            int id = Integer.parseInt(jTableTipoVeiculo.getValueAt(jTableTipoVeiculo.getSelectedRow(), 0).toString());
            TipoVeiculo tv = new TipoVeiculoDAO().buscarTipoVeiculoPorId(id);
            new JFrameCadastroTipoVeiculo(tv).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione algo para editar");
        }
        
    }//GEN-LAST:event_jButtonEditarTipoVeiculoActionPerformed

