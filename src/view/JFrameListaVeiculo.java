    private void jButtonEditarVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarVeiculoActionPerformed
        if (jTableVeiculo.getSelectedRow() > -1) {
            int id = Integer.parseInt(jTableVeiculo.getValueAt(jTableVeiculo.getSelectedRow(), 0).toString());
            Veiculo veiculo = new VeiculoDAO().buscarVeiculoPorId(id);
            new JFrameCadastroVeiculo(veiculo).setVisible(true);
        }else {
            JOptionPane.showMessageDialog(null, "Selecione algo para editar");
        }
    }//GEN-LAST:event_jButtonEditarVeiculoActionPerformed

