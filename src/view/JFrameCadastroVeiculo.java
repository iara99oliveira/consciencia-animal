    private void popularComponentes() {
        ArrayList<TipoVeiculo> tipoVeiculos = new TipoVeiculoDAO().retornarListaTipoVeiculo();
        jComboBoxTipoVeiculo.removeAllItems();
        for (TipoVeiculo tipoVeiculo : tipoVeiculos) {

            jComboBoxTipoVeiculo.addItem(tipoVeiculo);
        }
    }

