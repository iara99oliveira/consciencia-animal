    public void popularListaOcorrenciaPorPesquisa(String coluna, String valor) {
        DefaultTableModel modelo = (DefaultTableModel) jTableOcorrencia.getModel();
        ArrayList<Ocorrencia> ocorrencias = new OcorrenciaDAO().pesquisarOcorrencia(coluna, valor);
        modelo.setRowCount(0);
        for (Ocorrencia ocorrencia : ocorrencias) {
            modelo.addRow(new Object[]{
                ocorrencia.getIdOcorrencia(),
               Utilitarios.retornarPadraoBR( ocorrencia.getDataOcorrencia()),
                ocorrencia.getHoraOcorrencia(),
                ocorrencia.getLocalOcorrencia()
   
            });
        }
    }
    
    public void popularListaOcorrencia() {
        DefaultTableModel modelo = (DefaultTableModel) jTableOcorrencia.getModel();
        ArrayList<Ocorrencia> ocorrencias = new OcorrenciaDAO().retornarListaOcorrencia();
        modelo.setRowCount(0);
        for (Ocorrencia ocorrencia : ocorrencias) {
            modelo.addRow(new Object[]{
                ocorrencia.getIdOcorrencia(),
               Utilitarios.retornarPadraoBR( ocorrencia.getDataOcorrencia()),
                ocorrencia.getHoraOcorrencia(),
                ocorrencia.getLocalOcorrencia()
                
            });
        }
    }
    
    

