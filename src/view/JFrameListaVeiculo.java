    public void popularListaVeiculoPorPesquisa(String coluna, String valor, boolean disponibilidade) {
        DefaultTableModel modelo = (DefaultTableModel) jTableVeiculo.getModel();
        ArrayList<Veiculo> veiculos = new VeiculoDAO().pesquisarVeiculo(coluna, valor, disponibilidade);
        modelo.setRowCount(0);
        for (Veiculo veiculo : veiculos) {
            modelo.addRow(new Object[]{
                veiculo.getIdVeiculo(),
                veiculo.getModeloVeiculo(),
                veiculo.getPlacaVeiculo(),
                veiculo.getFabricanteVeiculo(),
                veiculo.isEstaDisponivel(),
                veiculo.getFKTipoVeiculo().getNomeTipoVeiculo()
            });
        }
    }
    
    public void popularListaVeiculos() {
        DefaultTableModel modelo = (DefaultTableModel) jTableVeiculo.getModel();
        ArrayList<Veiculo> veiculos = new VeiculoDAO().retornarListaVeiculo();
        modelo.setRowCount(0);
        for(Veiculo veiculo : veiculos){
            modelo.addRow(new Object[]{
                veiculo.getIdVeiculo(),
                veiculo.getModeloVeiculo(), 
                veiculo.getPlacaVeiculo(), 
                veiculo.getFabricanteVeiculo(), 
                veiculo.isEstaDisponivel(), 
                veiculo.getFKTipoVeiculo().getNomeTipoVeiculo()
            });
        }
    }    
    
